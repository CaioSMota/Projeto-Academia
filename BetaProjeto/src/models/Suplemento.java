package models;

public class Suplemento {

    private int idSuplemento;
    private String nomeSuplemento;
    private String descSuplemento;
    private String dosagemSuplemento;
    private String imgSuplemnto;

    public String getImgSuplemnto() {
        return imgSuplemnto;
    }

    public void setImgSuplemnto(String imgSuplemnto) {
        this.imgSuplemnto = imgSuplemnto;
    }

    public int getIdSuplemento() {
        return idSuplemento;
    }

    public void setIdSuplemento(int idSuplemento) {
        this.idSuplemento = idSuplemento;
    }

    public String getNomeSuplemento() {
        return nomeSuplemento;
    }

    public void setNomeSuplemento(String nomeSuplemento) {
        this.nomeSuplemento = nomeSuplemento;
    }

    public String getDescSuplemento() {
        return descSuplemento;
    }

    public void setDescSuplemento(String descSuplemento) {
        this.descSuplemento = descSuplemento;
    }

    public String getDosagemSuplemento() {
        return dosagemSuplemento;
    }

    public void setDosagemSuplemento(String dosagemSuplemento) {
        this.dosagemSuplemento = dosagemSuplemento;
    }

}
